   ,CAST(PA.DATPROXMAN AS DATE) AS 'DATA PRÓXIMA MANUTENÇÃO'
  FROM [SQLMML].[ENGEMAN].[engeman].PLAACU PA WITH (NOLOCK)
  INNER JOIN [SQLMML].[ENGEMAN].[engeman].PONCONACU PC WITH (NOLOCK)
    ON PC.CODAPL = PA.CODAPL
    AND PC.CODPONACU = PA.CODPONACU
  INNER JOIN [SQLMML].[ENGEMAN].[engeman].APLIC A WITH (NOLOCK)
    ON A.CODAPL = PC.CODAPL
  INNER JOIN [SQLMML].[ENGEMAN].[engeman].PLAMANUT PM WITH (NOLOCK)
    ON PM.CODPLA = PA.CODPLA
  INNER JOIN [SQLMML].[ENGEMAN].[engeman].EMPRESA E WITH (NOLOCK)
    ON E.CODEMP = A.CODEMP
  INNER JOIN [SQLMML].[ENGEMAN].[engeman].FILIAL F WITH (NOLOCK)
    ON F.CODFIL = A.CODFIL
    AND F.CODEMP = A.CODEMP
  WHERE PA.DATULTMAN IS NOT NULL
  AND EXISTS (SELECT
      1
    FROM [SQLMML].[ENGEMAN].[engeman].PLAXSET PS WITH (NOLOCK)
    INNER JOIN [SQLMML].[ENGEMAN].[engeman].SETEXE SE WITH (NOLOCK)
      ON SE.CODSET = PS.CODSET
    WHERE PS.CODPLA = PA.CODPLA)










